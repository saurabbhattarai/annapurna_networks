<script>
    let currentIndex = 0;
    let autoScrollInterval;

    function getItemsPerView() {
        if (window.innerWidth < 768) return 1;
        if (window.innerWidth < 1024) return 2;
        return 3;
    }

    function scrollTestimonials(direction) {
        const track = document.getElementById('testimonialsTrack');
        const cards = document.querySelectorAll('.testimonial-card');
        const itemsPerView = getItemsPerView();

        if (direction === 'left') {
            currentIndex = Math.max(0, currentIndex - itemsPerView);
        } else {
            currentIndex = Math.min(cards.length - itemsPerView, currentIndex + itemsPerView);
        }

        updateCarousel();
        resetAutoScroll();
    }

    function updateCarousel() {
        const track = document.getElementById('testimonialsTrack');
        const cards = document.querySelectorAll('.testimonial-card');
        const cardWidth = cards[0].offsetWidth;
        const gap = 24; // gap-6 = 1.5rem = 24px

        track.style.transform = `translateX(-${currentIndex * (cardWidth + gap)}px)`;

        // Update indicators
        const itemsPerView = getItemsPerView();
        const maxScroll = Math.ceil(cards.length / itemsPerView);
        const indicatorIndex = Math.min(Math.floor(currentIndex / itemsPerView), maxScroll - 1);

        document.querySelectorAll('[id^="indicator-"]').forEach((indicator, i) => {
            if (i === indicatorIndex) {
                indicator.classList.add('bg-blue-600');
                indicator.classList.remove('bg-gray-300');
            } else {
                indicator.classList.remove('bg-blue-600');
                indicator.classList.add('bg-gray-300');
            }
        });
    }

    function startAutoScroll() {
        autoScrollInterval = setInterval(() => {
            const track = document.getElementById('testimonialsTrack');
            const cards = document.querySelectorAll('.testimonial-card');
            const itemsPerView = getItemsPerView();

            if (currentIndex < cards.length - itemsPerView) {
                currentIndex += itemsPerView;
            } else {
                currentIndex = 0;
            }

            updateCarousel();
        }, 5000);
    }

    function resetAutoScroll() {
        clearInterval(autoScrollInterval);
        startAutoScroll();
    }

    // Initialize
    document.addEventListener('DOMContentLoaded', () => {
        updateCarousel();
        startAutoScroll();
    });

    // Handle window resize
    window.addEventListener('resize', () => {
        updateCarousel();
    });
</script>
