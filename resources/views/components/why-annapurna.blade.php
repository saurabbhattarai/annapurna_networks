<script>
    // Animated Counter Function
    function animateCounter(element, target, duration = 2000) {
        const increment = target / (duration / 16);
        let current = 0;

        const timer = setInterval(() => {
            current += increment;
            if (current >= target) {
                current = target;
                clearInterval(timer);
            }

            if (element.dataset.target === "99.99") {
                element.textContent = current.toFixed(2);
            } else if (element.dataset.target === "50") {
                element.textContent = Math.floor(current);
            } else {
                element.textContent = Math.floor(current) + '+';
            }
        }, 16);
    }

    // Intersection Observer for animations on scroll
    const observerOptions = {
        threshold: 0.3,
        rootMargin: '0px 0px -100px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting && !entry.target.dataset.animated) {
                entry.target.dataset.animated = true;

                // Animate counters
                const counters = entry.target.querySelectorAll('.counter');
                counters.forEach(counter => {
                    const target = parseFloat(counter.dataset.target);
                    animateCounter(counter, target);
                });

                // Animate cards
                const cards = entry.target.querySelectorAll('.group');
                cards.forEach((card, index) => {
                    setTimeout(() => {
                        card.style.animation = 'fadeInUp 0.6s ease-out forwards';
                    }, index * 100);
                });
            }
        });
    }, observerOptions);

    // Observe the entire component
    const component = document.querySelector('[data-why-annapurna]');
    if (component) observer.observe(component);
</script>

